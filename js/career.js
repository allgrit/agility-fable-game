const _lineCounters = {};
export function pickLine(context) {
  const pool = LINES[context];
  if (!pool || !pool.length) return null;
  const i = (_lineCounters[context] = ((_lineCounters[context] ?? -1) + 1) % pool.length);
  return pool[i];
}

export function startLineFor(mode, cls) {
  if (mode === 'worldcup') return pickLine('startWorldcup');
  const key = 'start' + cls.charAt(0).toUpperCase() + cls.slice(1);
  return LINES[key] ? pickLine(key) : null;
}

